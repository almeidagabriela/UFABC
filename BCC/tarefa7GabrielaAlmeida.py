#Estrutura condicional if: mostrar se um número inteiro é par ou impar

x = 1 #atribuindo o valor 1 a variavel x

if x%2 == 0: #Se o resto da divisão for igual a zero, então o número é par
    print('O número',x,'é par')
else: #Caso contrário, o número é impar
    print('O número',x,'é ímpar')
    
